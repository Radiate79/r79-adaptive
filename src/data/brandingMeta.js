/**
 * @param {string} version Raw semver or prefixed value (e.g. "1.0.0", "v1.0.0")
 * @returns {string} Major.minor display (e.g. "1.0")
 */
export function formatBrandVersion(version) {
  const raw = String(version).replace(/^v/i, "");
  const [major = "1", minor = "0"] = raw.split(".");
  return `${major}.${minor}`;
}

/**
 * @param {string} version
 * @returns {string} e.g. "Version 1.0"
 */
export function formatVersionLabel(version) {
  return `Version ${formatBrandVersion(version)}`;
}
